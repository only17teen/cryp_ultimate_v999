// === АТАКИ НА ИЗОГЕНИИ (ISOGENY ATTACKS) ===
// Главная причина осторожности:
// В 2022 году Castryck-Decru атаковали SIDH/SIKE классическим полиномиальным алгоритмом.
// Они использовали структуру изогений и torsion points (точки кручения) для восстановления секретной изогении.
// Атака работает за полиномиальное время и полностью ломает SIDH.
// Это показало, что supersingular isogeny problem может быть уязвим при определённых параметрах и структурах.

// Другие известные атаки на isogeny-based схемы:
// - Torsion point attacks и их обобщения
// - Атаки на конкретные реализации SIDH (side-channel, fault injection)
// - Анализ изогенного графа (isogeny graph) для поиска коротких путей
// - Quantum attacks (хотя проблема считается устойчивой к Shor'у, Grover может ускорять некоторые шаги)

// Вывод: После SIKE классические isogeny KEM (SIDH-style) считаются сломанными или слишком рискованными.
// Новые схемы должны избегать уязвимостей, которые позволили Castryck-Decru атаку.

// === АНАЛИЗ СТОЙКОСТИ SQISign ===
// SQISign — signature scheme на supersingular isogenies.
// Использует finding isogeny problem + дополнительную структуру для эффективной подписи.
// Текущий статус (на момент изучения):
// - Не сломан как SIKE. Активно исследуется и улучшается.
// - Security reduction к supersingular isogeny problem (с некоторыми дополнительными предположениями).
// - Параметры выбираются осторожно, чтобы избежать torsion point атак и подобных.
// - Преимущества: относительно компактные подписи по сравнению с некоторыми другими PQ схемами.
// - Риски: Всё ещё относительно новая область. Криптоанализ продолжается. Возможны будущие атаки на структуру SQISign.
// - Side-channel resistance: Требует аккуратной реализации (constant-time isogeny computations сложны).

// Рекомендация для cryp_ultimate_v999:
// - Isogeny-based (SQISign и новые варианты) использовать ТОЛЬКО как экспериментальный/высоко-параноидальный дополнительный слой.
// - Никогда не полагаться на него как на основной механизм (после SIKE история).
// - Лучше всего — в гипер-гибриде: X25519 + ML-KEM + (опционально SQISign/новая isogeny схема).
// - Для production malware сейчас безопаснее фокусироваться на lattice (ML-KEM/Dilithium) + hash-based (SPHINCS+).
// - Isogeny-based — для максимальной диверсификации hardness assumptions в самых параноидальных сценариях.

// Общий вывод после изучения атак:
// Пост-квантовая крипта — живая область. SIKE сломали, но lattice и hash-based держатся. Isogeny-based требует крайней осторожности.
// Гибридные конструкции с несколькими независимыми проблемами — лучший подход.

