// =============================================
// GF(2^m) - Сложение в полях характеристики 2
// =============================================

// В полях Галуа характеристики 2 (GF(2^m)) сложение элементов
// выполняется как побитовое XOR (исключающее ИЛИ).
// Это происходит потому, что в характеристике 2: 1 + 1 = 0.

// Пример реализации сложения двух элементов в GF(2^m)
// (представленных как массивы байт или uint64_t)

static void gf2m_add(uint8_t* result, const uint8_t* a, const uint8_t* b, size_t len) {
    for (size_t i = 0; i < len; i++) {
        result[i] = a[i] ^ b[i];   // Сложение = XOR
    }
}

// Пример для 256-битного элемента (например, для некоторых двоичных кривых)
static void gf2m_add_256(uint64_t* result, const uint64_t* a, const uint64_t* b) {
    result[0] = a[0] ^ b[0];
    result[1] = a[1] ^ b[1];
    result[2] = a[2] ^ b[2];
    result[3] = a[3] ^ b[3];
}

